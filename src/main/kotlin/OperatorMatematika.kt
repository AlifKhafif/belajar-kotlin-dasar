fun main(){

    //double
    val result = 10.0 / 3.0
    println(result)

    //int
    val result2 = 10 + 10 / 2
    println(result2)

    //Non Augmented Assigment
    var total: Int = 0
    val barang1 = 100
    total = total + barang1

    val barang2 = 200
    total = total + barang2

    val barang3 = 300
    total= total + barang3

    println(total)

    //Augmented Assigment
    var totalz: Int = 0
    val barangz1 = 100
    totalz += barangz1

    val barangz2 = 200
    totalz += barangz2

    val barangz3 = 300
    totalz += barangz3

    println(totalz)

   totalz++ // Total = total+1
    println(totalz)


}